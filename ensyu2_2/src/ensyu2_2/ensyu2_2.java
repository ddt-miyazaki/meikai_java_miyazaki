package ensyu2_2;

// 三つのint型変数に値を代入し、合計と平均を求めるプログラムを作成せよ。
public class ensyu2_2 {

	public static void main(String[] args) {
		// 変数の初期化
	    int x;
	    int y;
	    int z;

		// 変数への代入
	    x = 63;
	    y = 18;
	    z = 45;

		// 合計の表示
	    System.out.println("合計は" + (x + y + z) + "です。");
	    // 平均の表示
		System.out.println("平均は" + (x + y + z) / 2 + "です。");

	}

}
