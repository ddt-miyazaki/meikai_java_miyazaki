package ensyu5_3;

/*
	論理型の変数にtrueやfalseを代入して、その値を表示するプログラムを作成せよ
*/
public class ensyu5_3 {

	public static void main(String[] args) {
		// 論理型の変数にtrueを代入
		boolean t = true;
		// 論理型の変数にfalseを代入
		boolean f = false;
		// trueの値を表示
		System.out.println(t);
		// falseの値を表示
		System.out.println(f);

	}

}
